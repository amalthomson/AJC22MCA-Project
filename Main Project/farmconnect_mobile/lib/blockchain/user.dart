class User {
  final int id;
  final String name;
  final String email;
  final String phone;
  final String aadhar;
  final String address;
  final String dob;
  final String gender;

  User({
    required this.id,
    required this.name,
    required this.email,
    required this.phone,
    required this.aadhar,
    required this.address,
    required this.dob,
    required this.gender,
  });
}
